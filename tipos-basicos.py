#Utilizando a função type() para saber o tipo das variáveis

#String
print(type('Teste'))

#Inteiro
print(type(1))

#Float
print(type(1.1))

#Booleano
print(type(True))

#Booleano
print(type(False))

#Lista (Similar ao array em javascript)
print(type([1, 2, 3]))

#Dicionário (Similar ao objeto no javascript)
print(type({'nome': 'João', 'idade': 23}))